class ModulesZip:
    __slots__ = ('_v', '_props')
    # _init_dat: dict[_VERLIST, dict[_HASH_TYPE, str]]


    def __init__(self, version:str='latest', /):

        if version.lower() == 'latest':
            latest = next(reversed(self._init_dat))
            self._v, self._props = latest, self._init_dat[latest]
            return

        self._v = version
        self._props = self._init_dat[version]

    def ziphash(self, algorithm:str=...):
        return self._props.get(algorithm)

    def versions_list(self):
        return self._init_dat.keys()

    def download(self):

        msg = f' -- Download Modules {self._v} for Windows zip package'

        print(msg)

        download_zip = Path('.deps', self.zipname)

        if not download_zip.exists():

            # h = {
            #     'User-Agent':
            #     'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
            # }

            # req = request.Request(self.zipurl, headers=h)
            req = request.Request(self.zipurl)

            try:
                with request.urlopen(req) as response, open(download_zip, 'wb') as z:
                    z.write(response.read())
                message(f'{msg:<74} -- Download complete')

            except TimeoutError:
                raise TimeoutError(dedent(f'''\
                    WEMI cannot download Environment Modules zipfile {self.zipname} to
                    .deps folder with Timeout accident.
                    '''))
            except PermissionError:
                raise PermissionError(dedent(f'''\
                    WEMI cannot download Environment Modules zipfile {self.zipname} to
                    .deps folder with Internal PermissionError.'''))
        else:
            message(f'{msg:<74} -- Cached')

    def examine(self,
                hash_type:_HASH_TYPE='SHA256',
                chunk_size:Literal[65535]=65535):

        download_zip = Path('.deps', self.zipname)
        msg = f" -- Examine Modules {self._v} {download_zip} {hash_type}"
        message(msg)

        if not download_zip.exists():
            raise FileNotFoundError(f'Cannot examine expected file .deps/{self.zipname}.')

        if hash_type == 'CRC64':
            raise RuntimeError(dedent(f'''\
                Python Standard Library Hashlib/zlib doesn't support CRC64 examine.
                Please select SHA/SHA3/CRC32/MD5. '''))

        with open(download_zip, 'rb') as z:
            if hash_type == 'CRC32':
                crc_val = 0
                for chunk in iter(lambda: z.read(chunk_size), b''):
                    crc_val = zlib.crc32(chunk, crc_val)
                dz_hash = format(crc_val & 0xFFFFFFFF, '08x')
            else:
                try:
                    h = hashlib.new(hash_type)
                except ValueError:
                    raise ValueError(f"Not supported hash algorithm: {hash_type}")

                for chunk in iter(lambda: z.read(chunk_size), b""):
                    h.update(chunk)
                dz_hash = h.hexdigest().upper()

        res = (dz_hash == self._props.get(config.MODULE_ZIP_HASH_TYPE))

        message(f'{msg:<74} -- Pass' if res else f'{msg:<74} -- Failed')
        return res

    def unzip(self, dest:str='.deps', /):
        '''
        Args:
            zip_dir (str | Path):
            dest (str):
        '''
        msg = f' -- Decompress Modules {self._v} to {dest}/modules-{self._v}-win'
        message(msg)
        download_zip = Path('.deps', self.zipname)

        if not download_zip.exists():
            raise FileNotFoundError(f'Cannot found {download_zip}.')

        try:
            with zipfile.ZipFile(download_zip, 'r') as z:
                z.extractall(dest)
            print(f'{msg:<74} -- Complete')
        except PermissionError as e:
            raise PermissionError(f'Cannot unzip {download_zip}.')

    @property
    def version(self):
        return self._v

    @property
    def zipname(self):
        return f'modules-{self.version}-win.zip'

    @property
    def zipurl(self):
        v = self._v
        return f'https://github.com/envmodules/modules/releases/download/v{v}/modules-{v}-win.zip'

    @property
    def foldername(self):
        return f'modules-{self.version}-win'

